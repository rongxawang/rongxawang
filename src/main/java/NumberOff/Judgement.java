package NumberOff;

public class Judgement {

    public boolean judgeMultipleOf3(int number) {

        return number % 3 == 0;
    }

    public boolean judgeMultipleOf5(int number) {

        return number % 5 == 0;
    }

    public boolean judgeMultipleOf7(int number) {

        return number % 7 == 0;
    }
}
